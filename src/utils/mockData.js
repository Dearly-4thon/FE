export function sectionFromDate(date) {
  const m = date.getMonth() + 1;
  const d = date.getDate();
  
  if (m === 1 && d <= 15) return 'new-year';
  if (m >= 2 && m <= 6) return 'mid-1';
  if (m >= 7 && m <= 10) return 'mid-2';
  return 'year-end';
}

